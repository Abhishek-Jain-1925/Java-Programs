package loops_java;

import java.util.*;
public class print_n_times_msg 
{

	public static void main(String[] args) 
	{
		int i,n;
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter Value Of n");
		n=sc.nextInt();
		for(i=1;i<=n;i++)
		{
			System.out.println(i +":-"+" Welcome To Java Lang.");
		}
		
		
	}

}
